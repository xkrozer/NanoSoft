    private void aceptarBotonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_aceptarBotonActionPerformed
        // TODO add your handling code here
        
        if(estadoBoton.equals("agregar")){
            
            producto = new Producto();
            producto.setMarca(marcaTxt.getText());
            producto.setNombre(nombreTxt.getText());
            producto.setFabricante(fabricanteTxt.getText());
            producto.setDescripcion(descripcionTxt.getText());
            producto.setPrecio(Integer.parseInt(precioTxt.getText()));
            
            iniciarControlador();
            pjc.create(producto);
            JOptionPane.showMessageDialog(null,"Producto "+nombreTxt.getText()+" agregado correctamente");
            actualizarTabla();
        }
        
        else if(estadoBoton.equals("editar")){
            codigo = Integer.parseInt(tablaProducto.getValueAt(tablaProducto.getSelectedRow(),0).toString());
            
            iniciarControlador();
            producto = pjc.findProducto(codigo);
            producto.setMarca(marcaTxt.getText());
            producto.setNombre(nombreTxt.getText());
            producto.setFabricante(fabricanteTxt.getText());
            producto.setDescripcion(descripcionTxt.getText());
            producto.setPrecio(Integer.parseInt(precioTxt.getText()));
            
            try {
                pjc.edit(producto);
            } catch (Exception ex) {
                Logger.getLogger(Inventario.class.getName()).log(Level.SEVERE, null, ex);
            }
            
            JOptionPane.showMessageDialog(null,"Producto "+nombreTxt.getText()+" editado correctamente");
            actualizarTabla();
        }
        
        
        aceptarBoton.setVisible(false);
        cancelarBoton.setVisible(false);
    }//GEN-LAST:event_aceptarBotonActionPerformed

