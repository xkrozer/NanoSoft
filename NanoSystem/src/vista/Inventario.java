    private void eliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_eliminarActionPerformed
        // TODO add your handling code here:
        int fila = tablaProducto.getSelectedRow();
        if(fila>0){
            if(JOptionPane.showConfirmDialog(null,"¬øEst√° seguro que desea eliminar: "+tablaProducto.getValueAt(fila, 2)+"?","",JOptionPane.YES_NO_OPTION)==0){
                JOptionPane.showMessageDialog(null,tablaProducto.getValueAt(fila, 2) + " eliminado");
                iniciarControlador();
                try {
                    pjc.destroy(Integer.parseInt(tablaProducto.getValueAt(fila,0).toString()));
                } catch (NonexistentEntityException ex) {
                    Logger.getLogger(Inventario.class.getName()).log(Level.SEVERE, null, ex);
                }
                actualizarTabla();
            } 
        }
        else
            JOptionPane.showMessageDialog(null,"Primero seleccione una fila");
    }//GEN-LAST:event_eliminarActionPerformed

