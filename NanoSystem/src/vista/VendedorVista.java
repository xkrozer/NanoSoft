    private void cancelarProdBotonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cancelarProdBotonActionPerformed
        // TODO add your handling code here:
        
        int fila = tablaTicket.getSelectedRow();
        DefaultTableModel dtm = (DefaultTableModel) tablaTicket.getModel();
        if(fila>=0){
            subTotalN = subTotalN-Integer.parseInt(tablaTicket.getValueAt(fila,2).toString());
            ivaN = subTotalN * .16;
            totalN = subTotalN + ivaN;
            subtotalTxt.setText("$ "+String.format("%.2f", subTotalN));
            ivaTxt.setText("$ "+String.format("%.2f", ivaN));
            totalTxt.setText("$ "+String.format("%.2f", totalN));
            dtm.removeRow(fila);
        }
    }//GEN-LAST:event_cancelarProdBotonActionPerformed

