    private void agregarBotonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_agregarBotonActionPerformed
        // TODO add your handling code here:
        int fila = tablaProducto.getSelectedRow();
        DefaultTableModel dtm = (DefaultTableModel) tablaTicket.getModel();
        if(fila<0)
            JOptionPane.showMessageDialog(null,"Antes seleccione una fila");
        else{
            Object [] newRow = {tablaProducto.getValueAt(fila,0),tablaProducto.getValueAt(fila,2),tablaProducto.getValueAt(fila,5)};
            dtm.addRow(newRow);
            subTotalN = subTotalN+Integer.parseInt(tablaProducto.getValueAt(fila,5).toString());
            ivaN = subTotalN * .16;
            totalN = subTotalN + ivaN;
            subtotalTxt.setText("$ "+String.format("%.2f", subTotalN));
            ivaTxt.setText("$ "+String.format("%.2f", ivaN));
            totalTxt.setText("$ "+String.format("%.2f", totalN));
        }
    }//GEN-LAST:event_agregarBotonActionPerformed

